    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (txId.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "VocÃª nÃ£o pode gravar sem um codigo");
        } else if (txNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "VocÃª nÃ£o pode gravar sem um nome");
        } else if (txCnpj.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "VocÃª nÃ£o pode gravar sem um CNPJ");
        } else if (txCep.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "VocÃª nÃ£o pode gravar sem um CEP");
        } else if (txRua.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "VocÃª nÃ£o pode gravar sem uma rua");
        } else if (txNumero.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "VocÃª nÃ£o pode gravar sem um nÃºmero");
        } else if (txBairro.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "VocÃª nÃ£o pode gravar sem um bairro");
        } else if (txCidade.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "VocÃª nÃ£o pode gravar sem uma cidade");
        } else if (txEstado.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "VocÃª nÃ£o pode gravar sem uma estado");
        } else {
            PersistenciaFornecedor.save(CriarFornecedor());
            JOptionPane.showMessageDialog(null, "Cadastrado com Sucesso");
            desabilitarComponentes();
            limparCampos();
        }
    }//GEN-LAST:event_btSalvarActionPerformed

    private void txIdKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txIdKeyTyped
        char c = evt.getKeyChar();
        if (c < '0' || c > '9') {
            evt.consume();
        }
    }//GEN-LAST:event_txIdKeyTyped

    private void txCnpjKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txCnpjKeyTyped
        char c = evt.getKeyChar();
        if ((c < '0' || c > '9') && (c != '.') && (c!='/')) {
            evt.consume();
        }
    }//GEN-LAST:event_txCnpjKeyTyped

