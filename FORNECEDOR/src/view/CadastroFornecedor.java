    private Endereco CriarEndereco() {
        long Cep = Integer.parseInt(txCep.getText());
        String Rua = txRua.getText();
        int Numero = Integer.parseInt(txNumero.getText());
        String Bairro = txBairro.getText();
        String Cidade = txCidade.getText();
        String Estado = txEstado.getText();

        Endereco endereco = new Endereco(Cep, Rua, Numero, Bairro, Cidade, Estado);

        return endereco;
    }

    private Fornecedor CriarFornecedor() {
        int Id = Integer.parseInt(txId.getText());
        String Nome = txNome.getText();
        long Cnpj = Long.parseLong(txCnpj.getText());

        Fornecedor fornecedor = new Fornecedor(Id, Nome, Cnpj, CriarEndereco());

        return fornecedor;
    }

    private void limparCampos() {
        txId.setText("");
        txNome.setText("");
        txCnpj.setText("");
        txCep.setText("");
        txRua.setText("");
        txNumero.setText("");
        txBairro.setText("");
        txCidade.setText("");
        txEstado.setText("");
        btResgatar.removeAll();
        btCancelar.removeAll();
        btSalvar.removeAll();
    }

    public void resgatarFornecedor(Fornecedor a) {
        txId.setText(String.valueOf(a.getId()));
        txNome.setText(a.getNome());
        txCnpj.setText(String.valueOf(a.getCnpj()));
    }

    public void resgatarEndereco(Endereco a) {
        txCep.setText(String.valueOf(a.getCep()));
        txRua.setText(a.getRua());
        txNumero.setText(String.valueOf(a.getNumero()));
        txBairro.setText(a.getBairro());
        txCidade.setText(a.getCidade());
        txEstado.setText(a.getEstado());
    }

