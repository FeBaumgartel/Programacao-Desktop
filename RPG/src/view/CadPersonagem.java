    private void habilitarComponentes() {
        txId.setEnabled(true);
        txNome.setEnabled(true);
        txNivel.setEnabled(true);
        txVida.setEnabled(true);
        txAtaque.setEnabled(true);
        txDefesa.setEnabled(true);
        txExperiencia.setEnabled(true);
        txDinheiro.setEnabled(true);
        btCancelar.setEnabled(true);
        btSalvar.setEnabled(true);
    }

    private void desabilitarComponentes() {
        txId.setEnabled(false);
        txNome.setEnabled(false);
        txNivel.setEnabled(false);
        txVida.setEnabled(false);
        txAtaque.setEnabled(false);
        txDefesa.setEnabled(false);
        txExperiencia.setEnabled(false);
        txDinheiro.setEnabled(false);
        btCancelar.setEnabled(false);
        btSalvar.setEnabled(false);

    }

    private Personagem CriarPersonagem() {
        int Id = Integer.parseInt(txId.getText());
        String Nome = txNome.getText();
        int Nivel = Integer.parseInt(txNivel.getText());
        int Vida = Integer.parseInt(txVida.getText());
        int Ataque = Integer.parseInt(txAtaque.getText());
        int Defesa = Integer.parseInt(txDefesa.getText());
        double Experiencia = Double.parseDouble(txExperiencia.getText());
        double Dinheiro = Double.parseDouble(txDinheiro.getText());

        Personagem personagem1 = new Personagem(Id, Nome, Nivel, Vida, Ataque, Defesa, Experiencia, Dinheiro);

        return personagem1;
    }

    private void limparCampos() {
        txId.setText("");
        txNome.setText("");
        txNivel.setText("");
        txVida.setText("");
        txAtaque.setText("");
        txDefesa.setText("");
        txExperiencia.setText("");
        txDinheiro.setText("");

        btCancelar.removeAll();
        btSalvar.removeAll();
    }

    private TableModel carregarTabela() {
        List<Personagem> lista = PersistenciaPersonagem.list();
        ptm = new PersonagemTableModel(lista);
        return ptm;
    }

    private void atualizarTabela() {
        jTable1.setModel(carregarTabela());
    }

    private void recuperarPersonagem(Personagem a) {
        habilitarComponentes();
        txId.setText(String.valueOf(a.getId()));
        txNome.setText(a.getNome());
        txNivel.setText(String.valueOf(a.getNivel()));
        txVida.setText(String.valueOf(a.getVida()));
        txAtaque.setText(String.valueOf(a.getAtaque()));
        txDefesa.setText(String.valueOf(a.getDefesa()));
        txExperiencia.setText(String.valueOf(a.getExperiencia()));
        txDinheiro.setText(String.valueOf(a.getDinheiro()));
    }


