    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (txId.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você não pode gravar sem um codigo");
        } else if (txNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você não pode gravar sem um nome");
        } else if (txNivel.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você não pode gravar sem um nível");
        } else if (txVida.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você não pode gravar sem uma vida");
        } else if (txAtaque.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você não pode gravar sem um ataque");
        } else if (txDefesa.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você não pode gravar sem uma defesa");
        } else if (txExperiencia.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você não pode gravar sem uma experiência");
        } else if (txDinheiro.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Você não pode gravar sem um dinheiro");
        } else {
            PersistenciaPersonagem.save(CriarPersonagem());
            JOptionPane.showMessageDialog(null, "Personagem cadastrado com Sucesso");
            atualizarTabela();
            limparCampos();
            desabilitarComponentes();
        }
    }//GEN-LAST:event_btSalvarActionPerformed

